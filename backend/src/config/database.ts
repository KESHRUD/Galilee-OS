import mongoose from 'mongoose';

export const connectDB = async (): Promise<void> => {
  try {
    const mongoURI = process.env.MONGODB_URI || 'mongodb://localhost:27017/kanban-board';
    
    await mongoose.connect(mongoURI);
    
    console.log('‚úÖ MongoDB connected successfully');
    console.log(`üì¶ Database: ${mongoose.connection.db?.databaseName}`);
  } catch (error) {
    console.error('‚ùå MongoDB connection error:', error);
    process.exit(1);
  }
};

export const disconnectDB = async (): Promise<void> => {
  try {
    await mongoose.disconnect();
    console.log('‚úÖ MongoDB disconnected');
  } catch (error) {
    console.error('‚ùå MongoDB disconnect error:', error);
  }
};

// Handle connection events
mongoose.connection.on('connected', () => {
  console.log('üîó Mongoose connected to MongoDB');
});

mongoose.connection.on('error', (err) => {
  console.error('üî¥ Mongoose connection error:', err);
});

mongoose.connection.on('disconnected', () => {
  console.log('üîå Mongoose disconnected from MongoDB');
});

// Graceful shutdown
process.on('SIGINT', async () => {
  await mongoose.connection.close();
  console.log('üõë MongoDB connection closed due to app termination');
  process.exit(0);
});
